func (t *Tree) GetIndex(cont Container) int {
	nIndex := 0
	for e := t.Front(); e != nil; e = e.Next() {
		if e.Value == cont {
			return nIndex
		}
		nIndex++
	}

	//not found
	return -1
}

///////////////////////////////////////////////////////////////////////////////////////////////
//
///////////////////////////////////////////////////////////////////////////////////////////////
func (t *Tree) GetContainerByName(name string) Container {
	for e := t.Front(); e != nil; e = e.Next() {
		cont := e.Value.(Container)
		if cont.Name() == name {
			return cont
		}
	}

	return nil
}


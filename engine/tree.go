//
///////////////////////////////////////////////////////////////////////////////////////////////
func (t *Tree) TreeInsertBefore(cont Container, elm *list.Element) *list.Element {
	newElm := t.InsertBefore(cont, elm)
	t.RemoveUnmetDependency(cont.Name())
	return newElm
}

